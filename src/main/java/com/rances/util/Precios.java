package com.rances.util;

public class Precios {
	
	private static double hora = 5;
	
	private static double dia = 20;
	
	private static double semana = 60;
	
	private static double rentaFamiliar = 30;

	public static double getHora() {
		return hora;
	}

	public static void setHora(double hora) {
		Precios.hora = hora;
	}

	public static double getDia() {
		return dia;
	}

	public static void setDia(double dia) {
		Precios.dia = dia;
	}

	public static double getSemana() {
		return semana;
	}

	public static void setSemana(double semana) {
		Precios.semana = semana;
	}

	public static double getRentaFamiliar() {
		return rentaFamiliar;
	}

	public static void setRentaFamiliar(double rentaFamiliar) {
		Precios.rentaFamiliar = rentaFamiliar;
	}

	
	

}
